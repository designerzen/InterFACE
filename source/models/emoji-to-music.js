	if (harmonyOptions.harmonyMode === HARMONY_MODE_GLOBAL_KEY)
	{
		const context = getScaleContext(harmonyOptions.tonic, harmonyOptions.keyScale)
		const intervals = context.isChromatic ?
			getChromaticIntervalsForEmoji(emoji) :
			getChordDegreeOffsetsForEmoji(emoji)
		const cacheKey = `${tonic}|${emoji}|${harmonyOptions.tonic}|${harmonyOptions.keyScale}|${intervals.join(",")}`
		const cached = globalChordCache.get(cacheKey)
		if (cached)
		{
			return cached
		}
		const chord = getDiatonicChordInKey(tonic, harmonyOptions.tonic, harmonyOptions.keyScale, intervals)
		if (globalChordCache.size > MAX_GLOBAL_CHORD_CACHE_SIZE)
		{
			globalChordCache.clear()
		}
		globalChordCache.set(cacheKey, chord)
		return chord
	}
	
